@Misc.undoable
def flip_shape(axis: str):
    """Flip shape scale by given axis"""
    pm.undoInfo(ock=1)
    selection = pm.ls(sl=1, typ="transform", l=1)

    for sel in selection:
        # create tmp transform and clone shape
        tmp_transform = pm.duplicate(sel)[0]
        clone_shape([tmp_transform, sel])

        if pm.listRelatives(tmp_transform, c=1, typ="transform"):
            pm.delete(pm.listRelatives(tmp_transform, c=1, typ="transform"))

        list_lock = []
        for attr in ["sx", "sy", "sz"]:
            if pm.getAttr("{}.{}".format(tmp_transform, attr), l=1):
                list_lock.append("sx")
                pm.setAttr("{}.{}".format(tmp_transform, attr), l=0, k=1)

        pm.setAttr("{}.s{}".format(tmp_transform, axis), -1)
        pm.makeIdentity(tmp_transform, a=1, s=1)

        pm.select(sel, tmp_transform)
        clone_shape()
        pm.delete(tmp_transform)

    pm.select(selection)
    pm.undoInfo(cck=1)


