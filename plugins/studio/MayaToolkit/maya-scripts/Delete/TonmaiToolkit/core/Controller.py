def get_curve_color_data(shape):
    """
    Return curve color info based on override settings.

    Returns:
        [None, None]              -> override not enabled
        [True, index]             -> using color index
        [False, (r, g, b)]        -> using RGB color
    """
    if pm.objectType(shape) != "nurbsCurve":
        raise ValueError(f"{shape} is not a nurbsCurve")

    if not pm.getAttr(shape + ".overrideEnabled"):
        return [None, None]

    if pm.getAttr(shape + ".overrideRGBColors"):
        r = pm.getAttr(shape + ".overrideColorR")
        g = pm.getAttr(shape + ".overrideColorG")
        b = pm.getAttr(shape + ".overrideColorB")
        return [False, (r, g, b)]
    else:
        index = pm.getAttr(shape + ".overrideColor")
        return [True, index]


