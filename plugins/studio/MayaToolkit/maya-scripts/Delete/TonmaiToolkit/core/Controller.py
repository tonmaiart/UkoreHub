@Misc.undoable
def set_color(color_data, list_input_transform=None):
    """
    list_input_transform : support for both transform and shape.
    color_data(list) : [bool,integer index or list rgb] for example coloring index = [True,3] , coloring rgb = [False,(2.5,2.5,2.5)] and disable color = [None,None]

    """
    # get input control
    if list_input_transform:
        # get selection

        if not Utility.is_py_node(list_input_transform):
            list_target = [pm.PyNode(obj) for obj in list_input_transform]
        else:
            list_target = list_input_transform

    else:
        # get selection
        list_target = pm.ls(sl=1)

        # refine selection
        for sel in list_target:
            if "." in str(sel):
                isolate_clear()
                break

    list_shape = []

    # filter shape
    for sel in list_target:
        # sel shape
        if pm.objectType(sel, isa="nurbsCurve"):
            list_shape.append(sel)

        # child shape
        list_child = pm.listRelatives(sel, c=1, s=1, f=1)

        if list_child:
            for child in list_child:
                if pm.objectType(child) == "nurbsCurve":
                    list_shape.append(child)

    # apply color
    is_index, color_value = color_data

    if list_shape:
        for shape in list_shape:
            if not Utility.is_py_node(shape):
                shape = pm.PyNode(shape)

            shape = shape.name().split(".")[0]

            if color_data == [None, None]:
                pm.setAttr(shape + ".overrideEnabled", False)

            elif is_index:
                pm.setAttr(shape + ".overrideEnabled", True)
                pm.setAttr(f"{shape}.overrideRGBColors", 0)
                pm.setAttr(shape + ".overrideColor", color_value)

            elif not is_index:
                pm.setAttr(shape + ".overrideEnabled", True)
                pm.setAttr(f"{shape}.overrideRGBColors", 1)
                pm.setAttr(
                    shape + ".overrideColorRGB",
                    color_value[0],
                    color_value[1],
                    color_value[2],
                    typ="double3",
                )


@Misc.undoable
def create_control(index):
    selection = pm.ls(sl=1, typ="transform", l=1)

    if pm.ls(sl=1):
        replace = True
    else:
        replace = False

    dict_curve_data = get_official_curve_data(index=index)
    item_name = pm.PyNode(create_curve_from_data(dict_curve_data=dict_curve_data))

    if replace:  # replace mode
        if not selection:
            raise Exception("Please Select Targets before replace shape")

        list_ref_shape = pm.listRelatives(selection[0], c=1, typ="nurbsCurve")

        line_width = pm.getAttr(list_ref_shape[0] + ".lineWidth")
        color_index = get_curve_color_data(list_ref_shape[0])

        for sel in selection:
            clone_shape(
                [sel, item_name],
                line_width=line_width,
                color_data=color_index,
                match_bounding_box=True,
            )

        pm.select(selection, r=1)

        pm.inViewMessage(amg="Shape Updated.", pos="botCenter", fade=True)

        control = selection
    elif not replace:  # create mode
        pm.select(cl=1)

        control = pm.circle(ch=0)[0]
        clone_shape([control, item_name])

        pm.select(control)

        pm.inViewMessage(amg="Shape Created.", pos="botCenter", fade=True)

    # delete temp item
    pm.delete(item_name)

    return control


@Misc.undoable
def isolate_edit(snap=False, isolate=False):
    """Isolate panel , control vertex editing"""

    def get_curve_cv(shape_path):
        """return the list shape data (spans, degree, list_position) reference by input shape name

        shape_path : [str] shape name or path"""

        list_shape_data = []
        spans = pm.getAttr("{}.spans".format(shape_path))
        degree = pm.getAttr("{}.degree".format(shape_path))
        cv_amount = spans + degree
        list_position = []

        for i in range(cv_amount):
            local_translate = pm.xform(
                "{}.cv[{}]".format(shape_path, i), os=1, t=1, q=1
            )
            local_translate = ["%.4f" % value for value in local_translate]
            list_position.append(local_translate)

        list_shape_data.append(spans)
        list_shape_data.append(degree)
        list_shape_data.append(list_position)

        return list_shape_data

    selection = pm.ls(sl=1)
    pm.select(cl=1)

    for sel in selection:
        if pm.objectType(sel, isa="nurbsCurve"):
            pm.select(sel, add=1)

        child_shape = pm.listRelatives(sel, c=1, s=1, f=1)

        if child_shape:
            pm.select(child_shape, add=1)

    current_select = pm.ls(sl=1, typ="nurbsCurve")
    if current_select:
        if isolate:  # isolate
            currentPanel = pm.getPanel(withFocus=True)
            pm.isolateSelect(currentPanel, s=1)
            pm.isolateSelect(currentPanel, addSelected=True)

        if snap:  # Frame the selected objects in the viewport
            pm.viewFit()

    mel.eval("changeSelectMode -component;")

    # for shape in current_select:
    for shape in current_select:
        data = get_curve_cv(shape)
        pm.select("{}.cv[0:{}]".format(shape, data[0] + data[1] - 1), add=1)


